    public void displayTable(String username){
        ArrayList<Account> acc = conn.displayAccount(username);
        
        for(Account c : acc){
            String data[]={c.getAccountNumber(),Double.toString(c.getBalance())};
            tbl.addRow(data);
        }
    }
