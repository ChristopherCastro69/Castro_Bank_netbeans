    private void btnBalTransferActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBalTransferActionPerformed
        // TODO add your handling code here:
        int index = jTable1.getSelectedRow();
        try {
            String accNum = (String) jTable1.getValueAt(index, 0);
            double bal = Double.parseDouble((String)jTable1.getValueAt(index, 1));
            Account acc1;
            Account acc2;
            String accnum = tfAccNum.getText();
            double iBal = Double.parseDouble(tfBalance.getText());
            if(conn.checkAccount(accnum) == 1) {
                lblDisplay.setText("");
            } else {
                lblDisplay.setText("Incorrect Account number");
            }

            if(bal >= iBal) {
                acc1 = new Account(accNum, bal-iBal);
                jTable1.setValueAt(Double.toString(bal - iBal), index, 1);
                if(conn.getAccountUsername(accNum).equals(lblUsername.getText())) {
                    for(int i=0; i<jTable1.getRowCount(); i++) {
                        for(int j=0; j<jTable1.getColumnCount(); j++) {
                            String temp = (String) jTable1.getValueAt(i, j);
                            if(accnum.equals(temp)) {
                                jTable1.setValueAt(Double.toString(conn.getAccountBalance(accnum)+iBal),i, j+1);
                                break;
                            }
                        }
                    }
                }
                conn.updateBalance(acc1);
                acc2 = new Account(accnum, conn.getAccountBalance(accnum)+iBal);
                conn.updateBalance(acc2);

            } else {
                lblDisplay.setText("Insufficient Balance");
            }
        } catch(IllegalArgumentException i) {
            lblDisplay.setText("Please input a valid amount");
        }
    }//GEN-LAST:event_btnBalTransferActionPerformed
    
